"""Utilities to generate API documentation text from grouped metadata."""

from __future__ import annotations

import inspect
import re


def _underline(text: str, char: str) -> str:
    return char * len(text)


def _slugify(text: str) -> str:
    """Convert text into a stable anchor-friendly slug.

    Lowercases the input, replaces non-alphanumeric characters with ``-``,
    and trims leading/trailing dashes.
    Example: ``"Tracking & Analysis" -> "tracking-analysis"``.
    """
    slug = re.sub(r"[^a-z0-9]+", "-", text.lower()).strip("-")
    return slug or "section"


def _wrap_friendly(text: str) -> str:
    # Add zero-width break opportunities for long snake_case names.
    return text.replace("_", "_\u200b")


def _first_sentence(doc: str | None) -> str:
    if not doc:
        return "TBD"
    paragraph_lines = []
    for raw_line in doc.splitlines():
        line = raw_line.strip()
        if not line:
            if paragraph_lines:
                break
            continue
        paragraph_lines.append(line)

    if not paragraph_lines:
        return "TBD"

    out = " ".join(paragraph_lines).strip()
    return _wrap_friendly(out) if out else "TBD"


def _append_summary_table_style(out: list[str], table_class: str) -> None:
    out.append(".. raw:: html\n\n")
    out.append("   <style>\n")
    out.append(f"   table.{table_class} {{ width: 100%; table-layout: fixed; }}\n")
    out.append(f"   table.{table_class} th,\n")
    out.append(f"   table.{table_class} td {{\n")
    out.append("     white-space: normal !important;\n")
    out.append("     overflow-wrap: anywhere;\n")
    out.append("     word-break: break-word;\n")
    out.append("   }\n")
    out.append(f"   table.{table_class} caption {{\n")
    out.append("     font-size: 1.10em !important;\n")
    out.append("     font-weight: 600 !important;\n")
    out.append("   }\n")
    out.append(f"   table.{table_class} caption .caption-text,\n")
    out.append(f"   table.{table_class} caption .caption-text a {{\n")
    out.append("     font-size: 1.10em !important;\n")
    out.append("     font-weight: 600 !important;\n")
    out.append("     font-style: normal !important;\n")
    out.append("     line-height: 1.2 !important;\n")
    out.append("   }\n")
    out.append("   </style>\n\n")


def _iter_public_members(cls, *, include_properties: bool = True):
    for name, member in cls.__dict__.items():
        if name.startswith("_"):
            continue

        if isinstance(member, property):
            if include_properties:
                yield "property", name, member
            continue

        if isinstance(member, (classmethod, staticmethod)):
            yield "method", name, member.__func__
            continue

        if (
            inspect.isfunction(member)
            or inspect.ismethoddescriptor(member)
            or inspect.isbuiltin(member)
            or inspect.isroutine(member)
        ):
            yield "method", name, member


def generate_class_rst(
    cls,
    *,
    include_properties: bool = True,
    include_summary_table: bool = True,
) -> str:
    """Generate RST for a class without doc groups metadata."""
    full_name = f"{cls.__module__}.{cls.__name__}"
    class_slug = _slugify(cls.__name__)
    class_label = f"class-api-{class_slug}"
    members = list(_iter_public_members(cls, include_properties=include_properties))
    members_sorted = sorted(members, key=lambda item: item[1].lower())

    out = []
    out.append(f".. This file content is autogenerated from ``{full_name}``.\n\n")

    if include_summary_table:
        out.append("Members - short description\n")
        out.append("--------------------------\n\n")
        _append_summary_table_style(out, "class-api-summary-table")
        out.append(".. list-table:: Members\n")
        out.append("   :class: class-api-summary-table\n")
        out.append("   :header-rows: 1\n")
        out.append("   :width: 100%\n")
        out.append("   :widths: 35 65\n\n")
        out.append("   * - Member\n")
        out.append("     - Description\n")

        if not members_sorted:
            out.append("   * - -\n")
            out.append("     - -\n")
        else:
            for member_kind, member_name, member_obj in members_sorted:
                member_label = f"{class_label}-{member_kind}-{_slugify(member_name)}"
                if member_kind == "method":
                    member_text = _wrap_friendly(f"{member_name}(...)")
                else:
                    member_text = _wrap_friendly(member_name)
                out.append(f"   * - :ref:`{member_text} <{member_label}>`\n")
                out.append(f"     - {_first_sentence(inspect.getdoc(member_obj))}\n")
        out.append("\n")

    out.append("Members - full description\n")
    out.append("--------------------------\n\n")
    out.append(f".. _{class_label}:\n\n")
    out.append(f"{cls.__name__}\n")
    out.append(f"{_underline(cls.__name__, '~')}\n\n")

    if not members_sorted:
        out.append(f".. autoclass:: {full_name}\n")
        out.append("    :members:\n")
        out.append("    :member-order: bysource\n\n")
    else:
        for member_kind, member_name, _ in members_sorted:
            member_label = f"{class_label}-{member_kind}-{_slugify(member_name)}"
            out.append(f".. _{member_label}:\n\n")
            if member_kind == "method":
                out.append(f".. automethod:: {full_name}.{member_name}\n\n")
            else:
                out.append(f".. autoproperty:: {full_name}.{member_name}\n\n")

    return "".join(out)


def generate_grouped_class_rst(
    cls,
    *,
    title: str | None = None,
    include_properties: bool = True,
    include_toc: bool = False,
    include_summary_table: bool = True,
) -> str:
    """Generate RST for a grouped class API.

    Parameters
    ----------
    cls : type
        Class exposing ``__doc_groups__`` metadata.
    title : str, optional
        Kept for backward compatibility; ignored in output generation.
    include_properties : bool, optional
        If True, include property sections.
    include_toc : bool, optional
        Kept for backward compatibility; ignored in output generation.
    include_summary_table : bool, optional
        If True, include a compact summary table of groups and members.

    Returns
    -------
    str
        Full reStructuredText content.
    """
    groups = getattr(cls, "__doc_groups__", None)
    if groups is None:
        raise ValueError(
            f"{cls.__name__} has no __doc_groups__ metadata. "
            "Ensure doc groups are initialized."
        )

    full_name = f"{cls.__module__}.{cls.__name__}"
    out = []
    out.append(
        f".. This file content is autogenerated from ``{full_name}.__doc_groups__``.\n\n"
    )
    if include_summary_table:
        out.append("Members - short description\n")
        out.append("--------------------------\n\n")
        _append_summary_table_style(out, "line-api-summary-table")
        for group in groups:
            cat_name = group["name"]
            methods = sorted(group.get("methods", []))
            properties = sorted(group.get("properties", []))
            cat_slug = _slugify(cat_name)
            cat_label = f"line-api-{cat_slug}"
            summary_label = f"{cat_label}-summary"

            out.append(f".. _{summary_label}:\n\n")
            out.append(f".. list-table:: :ref:`{cat_name} <{cat_label}>`\n")
            out.append("   :class: line-api-summary-table\n")
            out.append("   :header-rows: 1\n")
            out.append("   :width: 100%\n")
            out.append("   :widths: 40 60\n\n")
            out.append("   * - Member\n")
            out.append("     - Description\n")

            for method in methods:
                member_obj = getattr(cls, method)
                desc = _first_sentence(inspect.getdoc(member_obj))
                method_text = _wrap_friendly(f"{method}(...)")
                out.append(
                    f"   * - :ref:`{method_text} <{cat_label}-method-{_slugify(method)}>`\n"
                )
                out.append(f"     - {desc}\n")

            for prop in properties:
                member_obj = getattr(cls, prop)
                desc = _first_sentence(inspect.getdoc(member_obj))
                prop_text = _wrap_friendly(prop)
                out.append(
                    f"   * - :ref:`{prop_text} <{cat_label}-property-{_slugify(prop)}>`\n"
                )
                out.append(f"     - {desc}\n")

            if not methods and not properties:
                out.append("   * - -\n")
                out.append("     - -\n")
            out.append("\n")
        out.append("\n")

    out.append("Members - full description\n")
    out.append("--------------------------\n\n")

    for group in groups:
        cat_name = group["name"]
        methods = sorted(group.get("methods", []))
        properties = sorted(group.get("properties", []))
        cat_slug = _slugify(cat_name)
        cat_label = f"line-api-{cat_slug}"
        methods_label = f"{cat_label}-methods"
        properties_label = f"{cat_label}-properties"

        out.append(f".. _{cat_label}:\n\n")
        out.append(f"{cat_name}\n")
        out.append(f"{_underline(cat_name, '~')}\n\n")
        if include_summary_table:
            out.append(f"Go to :ref:`Summary table <{cat_label}-summary>`\n\n")

        if methods:
            out.append(f".. _{methods_label}:\n\n")
            for method in methods:
                out.append(f".. _{cat_label}-method-{_slugify(method)}:\n\n")
                out.append(f".. automethod:: {full_name}.{method}\n\n")

        if include_properties and properties:
            out.append(f".. _{properties_label}:\n\n")
            for prop in properties:
                out.append(f".. _{cat_label}-property-{_slugify(prop)}:\n\n")
                out.append(f".. autoproperty:: {full_name}.{prop}\n\n")

    return "".join(out)


def generate_line_rst(
    *,
    title: str = "Line API (Grouped)",
    include_properties: bool = True,
    include_toc: bool = False,
    include_summary_table: bool = True,
) -> str:
    """Generate RST documentation for ``xtrack.Line`` doc groups."""
    from .line import Line

    return generate_grouped_class_rst(
        Line,
        title=title,
        include_properties=include_properties,
        include_toc=include_toc,
        include_summary_table=include_summary_table,
    )


def generate_environment_rst(
    *,
    include_properties: bool = True,
    include_summary_table: bool = True,
) -> str:
    """Generate RST documentation for ``xtrack.Environment``."""
    from .environment import Environment

    return generate_grouped_class_rst(
        Environment,
        include_properties=include_properties,
        include_summary_table=include_summary_table,
    )
